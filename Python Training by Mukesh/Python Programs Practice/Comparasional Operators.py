# Comparisional operators 

# '==' and '!=' operators

a = 25
b = 50
if (a==b):
	print("a == b")
else:
	print("a !=  b")

# '>' and '<' operators 

x = 350
y = 500
if (x>y):
	print("x  >  y")
else:
	print("x  < y  ")

# '>= and <= Operators'

p = 15
q = 15
if (p>=q):
	print("p >= q")
else:
	print("p <= q")

